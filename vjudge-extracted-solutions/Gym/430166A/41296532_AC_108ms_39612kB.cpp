const ll mod = 100000007;
const int N = 1000+9;
int p[N], c[N], memo[N][10009];
int n, k;

int wag3(int i, int rem){
    if(rem < 0)
        return -1e7;
    if(i == n)
        return 0;

    int &ret = memo[i][rem];
    if(~ret)
        return ret;

    int ch1 = c[i] + wag3(i+1, rem-p[i]);
    int ch2 = wag3(i+1, rem);

    return ret = max(ch1, ch2);
}

int main() {
    KHALED_WALEED_ATTIA
    fileInput();
    cin >>n >>k;
    memset(memo, -1, sizeof memo);
    for(int i = 0; i < n; i++)
        cin >>p[i];
    for(int i = 0; i < n; i++)
        cin >>c[i];
    cout<<wag3(0, k)<<endl;
    return 0;
}

