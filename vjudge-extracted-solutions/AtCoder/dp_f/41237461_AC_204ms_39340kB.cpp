int memo[N][N];
string s, t;

int lcs(int i, int j){
    if(i == s.size() || j == t.size())
        return 0;

    int & ret = memo[i][j];
    if(~ret)
        return ret;

    int ch1 = 0, ch2 = 0, ch3 = 0;
    if(s[i] == t[j])
         ch1 = 1 + lcs(i+1, j+1);
    ch2 = lcs(i+1, j);
    ch3 = lcs(i, j+1);

    return ret = max({ch1, ch2, ch3});
}

void printLcs(int i, int j){
    if(i == s.size() || j == t.size())
        return ;

    int ret = lcs(i, j);

    int ch1 = 0, ch2 = 0, ch3 = 0;
    if(s[i] == t[j]){
        ch1 = 1 + lcs(i+1, j+1);
        if(ch1 == ret){
            cout<<s[i];
            printLcs(i+1, j+1);
            return;
        }
    }
    ch2 = lcs(i+1, j);
    ch3 = lcs(i, j+1);
    if(ch2 == ret){
        printLcs(i+1, j);
        return;
    }
    if(ch3 == ret){
        printLcs(i, j+1);
        return;
    }
}

