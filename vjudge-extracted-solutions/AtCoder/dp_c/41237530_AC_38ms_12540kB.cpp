const int N=(int) 1e5 + 5;
int memo[N][4];
int a[N], b[N], c[N];
int n;

int maxHappiness(int i, int lastAc){
    if(i == n)
        return 0;

    int & ret = memo[i][lastAc];
    if(~ret)
        return ret;

    int ch1 = -1, ch2 = -1, ch3 = -1;
    if(lastAc != 1)
        ch1 = a[i] + maxHappiness(i+1, 1);
    if(lastAc != 2)
        ch2 = b[i] + maxHappiness(i+1, 2);
    if(lastAc != 3)
        ch3 = c[i] + maxHappiness(i+1, 3);

    return ret = max({ch1, ch2, ch3});
}

