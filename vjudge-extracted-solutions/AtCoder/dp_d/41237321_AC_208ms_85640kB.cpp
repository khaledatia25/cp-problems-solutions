ll memo[N][W];
int n, w;
vector<ll>we, vals;
ll rec(int i, int rem){
    if(rem < 0)
        return -1e17;
    if(i == n)
        return 0;

    ll&ret = memo[i][rem];
    if(~ret)
        return ret;

    ret = vals[i]+rec(i+1, rem-we[i]);
    ret = max(ret, rec(i+1, rem));

    return ret;
}
