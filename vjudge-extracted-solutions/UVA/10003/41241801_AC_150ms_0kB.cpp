int rec(int i, int j, int ci, int cj){
    if(i == j || ci > cj)
        return 0;

    int & ret = memo[ci][cj];
    if(~ret)
        return ret;

    ret = INT_MAX;

    for(int k = ci; k <= cj; k++){
        ret = min(ret, rec(i, cuts[k], ci, k-1) + rec(cuts[k]+1, j, k+1, cj) + j-i+1);
    }
    return ret;
}
