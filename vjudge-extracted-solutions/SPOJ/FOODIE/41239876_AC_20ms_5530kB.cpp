const int dx[] = {0, 0, 1, -1, 1, -1, 1, -1},
        dy[] = {1, -1, 0, 0, 1, -1, -1, 1};
const ll mod = 1000000007;
const int N = 100+9, K = 1000+9;
int racks[N];
int memo[N][K];
int n, k;
int naruto(int i, int rem){
    if(rem < 0)
        return -1e5;
    if(i == n)
        return 0;

    int & ret = memo[i][rem];
    if(~ret)
        return ret;

    int ch1 = racks[i] + naruto(i+1, rem-racks[i]);
    int ch2 = naruto(i+1, rem);

    return ret = max(ch1, ch2);
}
int main() {
    KHALED_WALEED_ATTIA
    fileInput();
    int t;
    cin >> t;
    while(t--){
        memset(memo, -1, sizeof memo);
        cin >>n >>k;
        for(int i = 0; i< n; i++){
            int x ,y, sum = 0;
            cin >>x;
            while(x--)cin >>y, sum+=y;
            racks[i] = sum;
        }
        cout<<naruto(0, k)<<endl;
        if(t)cout<<endl;
    }
    return 0;
}









