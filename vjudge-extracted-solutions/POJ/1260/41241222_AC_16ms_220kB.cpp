const int N = 100+9;
int memo[N], p[N], a[N];
int n;
int naruto(int i){
    if(i == n)
        return 0;

    int &ret = memo[i];
    if(~ret)
        return ret;

    int sum = 0;
    ret = INT_MAX;
    for(int j = i; j < n; j++){
        sum += a[j];
        ret = min(ret, (sum + 10) * p[j] + naruto(j+1));
    }
    return ret;
}
